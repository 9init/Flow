func generateRandomStr(length int) string {
	str := "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789"
	generated := ""
	for i := 0; i < length; i++ {
		rand.Seed(time.Now().UnixNano())
		num := rand.Intn(length) + 1
		generated += str[:num]
	}
	return generated
}

func addHours(tTime time.Time, hours time.Duration) string {
	loc, _ := time.LoadLocation("UTC")
	t := tTime.In(loc).Add(hours * time.Hour).Format("20060102150405")
	return t
}

func addMinutes(tTime time.Time, minutes time.Duration) string {
	loc, _ := time.LoadLocation("UTC")
	t := tTime.In(loc).Add(minutes * time.Minute).Format("20060102150405")
	return t
}

